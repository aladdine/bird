(m + n) + p = m + (n + p)
p = Zero:
left part: (m + n) + Zero = (m + n) = m + n
right part: m + (n + Zero) = m + n

let's suppose (m + n) + p = m + (n + p), for all p <= x
p = Succ x
left part: (m + n) + Succ x = Succ ((m + n) + x)
right part: m + (n + Succ x) = m + Succ (n + x) = Succ (m + (n + x))

